package kr.co.mlec.repository.domain;

public class Memo {
	private int memoNo;
	private String memoContent;
	private String memoX;
	private String memoY;
	private String memoColor;
	private int pixWhe;
	private int foldWhe;
	
	
	public int getMemoNo() {
		return memoNo;
	}
	public void setMemoNo(int memoNo) {
		this.memoNo = memoNo;
	}
	public String getMemoContent() {
		return memoContent;
	}
	public void setMemoContent(String memoContent) {
		this.memoContent = memoContent;
	}
	public String getMemoX() {
		return memoX;
	}
	public void setMemoX(String memoX) {
		this.memoX = memoX;
	}
	public String getMemoY() {
		return memoY;
	}
	public void setMemoY(String memoY) {
		this.memoY = memoY;
	}
	public String getMemoColor() {
		return memoColor;
	}
	public void setMemoColor(String memoColor) {
		this.memoColor = memoColor;
	}
	public int getPixWhe() {
		return pixWhe;
	}
	public void setPixWhe(int pixWhe) {
		this.pixWhe = pixWhe;
	}
	public int getFoldWhe() {
		return foldWhe;
	}
	public void setFoldWhe(int foldWhe) {
		this.foldWhe = foldWhe;
	}


}
